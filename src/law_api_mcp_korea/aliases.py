def resolve_alias(query: str) -> str:
    """약어를 포함한 검색어를 정식 법령명으로 확장한다. 매칭 없으면 원본 반환."""
    return LAW_ALIASES.get(query.strip(), query)


def not_found_response(query: str, domain: str = "") -> dict[str, Any]:
    """검색 결과가 없을 때 반환할 표준 NOT_FOUND 응답 dict."""
    domain_str = f"{domain} " if domain else ""
    return {
        "not_found": True,
        "marker": NOT_FOUND_MARKER,
        "query": query,
        "domain": domain,
        "message": (
            f"{NOT_FOUND_MARKER} '{query}'에 해당하는 {domain_str}결과가 없습니다. "
            "검색어나 법령명을 확인하세요."
        ),
    }
